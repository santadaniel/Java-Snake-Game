package Swing;

/**
 * The State enum represents different states in the Snake game.
 * It includes states for the main menu, one-player mode, two-player mode, and the leaderboard.
 */
public enum State
{
    /**
     * Menu state.
     */
    Menu,
    /**
     * One player state.
     */
    OnePlayer,
    /**
     * Two players state.
     */
    TwoPlayers,
    /**
     * Leader board state.
     */
    LeaderBoard
}
