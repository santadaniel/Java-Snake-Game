package Game;

/**
 * The Direction enum represents the possible movement directions for the Snake in the game.
 * It includes UP, DOWN, RIGHT, and LEFT.
 */
public enum Direction
{
    /**
     * Up direction.
     */
    UP,
    /**
     * Down direction.
     */
    DOWN,
    /**
     * Right direction.
     */
    RIGHT,
    /**
     * Left direction.
     */
    LEFT
}
